    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        //Initializes Array for Cases
        Double[] values = {0.01, 1.0, 10.0, 50.0, 100.0, 500.0, 1000.0, 5000.0, 10000.0, 25000.0, 50000.0, 100000.0};
        Boolean[] valUsed = {false, false, false, false, false, false, false, false, false, false, false, false};
        Random rand = new Random();

        caseArr = new Case[12];
        
        for(int i = 0; i < 12; i++){
            Boolean valNotChosen = true;
            int hold = -1;
            do {
                hold = rand.nextInt(12);
                if (valUsed[hold] == false){
                    valNotChosen = false;
                }
            } while(valNotChosen);
            caseArr[i] = new Case(values[hold]);
            valUsed[hold] = true;
        }
        
        //Initializes Turn value
        turn = 0;
        
        //Initializes AverageVal value
        averageVal = 0.0;
        
        //Open MoneyView Form
        this.setVisible(false);
        new frmMoneyView().setVisible(true);
    }//GEN-LAST:event_btnStartActionPerformed

