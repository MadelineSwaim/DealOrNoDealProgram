    //Calculates the average value of the unopened cases and sets the AverageVal variable equal to it.
    public static void calcAverage(){
        Double sum = 0.0;
        int count = 0;
        
        for (int i = 0; i < 12; i++){
            if (caseArr[i].isCaseOpen() == false){
                sum += caseArr[i].getValue();
                count++;
            }
        }
        
        averageVal = sum / count;
    }

