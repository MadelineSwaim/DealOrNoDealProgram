    private void btnOpenCaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenCaseActionPerformed
        int turnHold = Opening.turn - 1;
        if (turnHold >= 0 && turnHold < callBanker.length){
            if (callBanker[turnHold]){
                //Go To Banker Call form
                this.setVisible(false);
                new frmBankerCall().setVisible(true);
            }
            else{
                //Go To Case View form
                this.setVisible(false);
                new frmCaseView().setVisible(true);
            }
        }
        else if (turnHold >= callBanker.length){
            this.setVisible(false);
            new frmFinalTwoCases().setVisible(true);
        }
        else{
            //Go To Case View form
            this.setVisible(false);
            new frmCaseView().setVisible(true);
        }
    }//GEN-LAST:event_btnOpenCaseActionPerformed

    private void btnEndGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEndGameActionPerformed
        if (Opening.turn > 0){
            int pCase = -1;
            for(int i = 0; i < 12; i++){
                if (Opening.caseArr[i].isThisPlayersCase()){
                    pCase = i;
                }
            }
            frmEndGame.winnings = Opening.caseArr[pCase].getValue();
            new frmEndGame().setVisible(true);
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "You must pick a case to end the game early in this \nway. Close Game Window to end program Now.");
        }
        
    }//GEN-LAST:event_btnEndGameActionPerformed

