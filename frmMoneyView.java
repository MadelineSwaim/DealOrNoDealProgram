    public frmMoneyView() {
        initComponents();
        
        //If it's first time opening this window, the OpenCase Button will say Pick Case to indicate that the player is picking their case in the next window
        if (Opening.turn == 0){
            btnOpenCase.setText("Pick Case");
        }
        else{
            //This will make the opened values appear gray
            //(Unfortunately, Doubles don't work with Switch statements, so I had to use If statements)
            for(int i = 0; i < 12; i++){
                if (Opening.caseArr[i].isCaseOpen()){
                    if(Opening.caseArr[i].getValue() == 0.01){
                        txt1Penny.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 1.0){
                        txt1Dollar.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 10.0){
                        txt10Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 50.0){
                        txt50Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 100.0){
                        txt100Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 500.0){
                        txt500Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 1000.0){
                        txt1000Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 5000.0){
                        txt5000Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 10000.0){
                        txt10000Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 25000.0){
                        txt25000Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 50000.0){
                        txt50000Dollars.setBackground(Color.gray);
                    }
                    else if(Opening.caseArr[i].getValue() == 100000.0){
                        txt100000Dollars.setBackground(Color.gray);
                    }
                }
            }
            int turnHold = Opening.turn - 1;
            if (turnHold < callBanker.length && callBanker[turnHold]){
                btnOpenCase.setText("Call Banker");
            }
            else if(turnHold >= callBanker.length){
                btnOpenCase.setText("Pick Case");
            }
        }
    }

